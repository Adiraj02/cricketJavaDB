    public ArrayList<MatchReport> getMatchList()
    {
            ArrayList<MatchReport> matchList  = new ArrayList<MatchReport>();
            Connection con = getConnection();
            String query = "SELECT * FROM match_report";
            
            Statement st;
            ResultSet rs;
            
        try {
            
            st = con.createStatement();
            rs = st.executeQuery(query);
            MatchReport m1;
            
            while(rs.next())
            {
                m1 = new MatchReport(rs.getInt("match_id"),rs.getString("result"),rs.getString("mom"));
                matchList.add(m1);
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
        return matchList;  
                
    }
    
    
