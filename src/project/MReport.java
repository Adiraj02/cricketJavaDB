    public void Show_MatchReport_In_JTable()
    {
        ArrayList<MatchReport> list = getMatchList();
        DefaultTableModel model = (DefaultTableModel)JTable_MatchReport.getModel();
        // clear jtable content
        model.setRowCount(0);
        Object[] row = new Object[3];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getResult();
            row[2] = list.get(i).getMOM();
            
            model.addRow(row);
        }
    
    }
